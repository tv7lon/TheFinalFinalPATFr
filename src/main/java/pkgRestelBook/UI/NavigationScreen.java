    private void viewBookRevButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBookRevButtonActionPerformed
        // TODO add your handling code here:
        Enumeration<AbstractButton> viewForBG = bookingOrReviewButtonGroup.getElements();
        String viewForStr = ViewingManager.getBookOrReview(viewForBG);
        String viewForItemStr = (String) viewItemForCombo.getSelectedItem();

        //check which to show
        // REVIEWS //
        if (viewForStr.equals("Previous Reviews")) {
            viewReviewForLabel.setText(viewForItemStr);

            if (ReviewsManager.getTxtFileScore(viewForItemStr).isBlank() || (ReviewsManager.getTxtFileScore(viewForItemStr).isEmpty())) {
                displayScoreLabel.setText("No entry yet.");
            } else {
                displayScoreLabel.setText(ReviewsManager.getTxtFileScore(viewForItemStr));
            }
            
            if ((ReviewsManager.getCommentFromTxt(viewForItemStr)).isBlank()||((ReviewsManager.getCommentFromTxt(viewForItemStr)).isEmpty())) {
                commentDisplayLabel.setText("No entry yet.");
            } else {
                commentDisplayLabel.setText(ReviewsManager.getCommentFromTxt(viewForItemStr));
            }
            

            //show the screen
            navigationScreenTabbedPane.add("Previous Review Receipt", viewReviewPanel);
        } else {

            // BOOKINGS //
            //HOTEL BOOKING
            boolean isHotel = BookingsManager.checkIfHotel(viewForItemStr);

            //display everything
            if (isHotel) {
                String viewBookingForStr = (String) viewItemForCombo.getSelectedItem();
                hotelDisplayNameLabelBook.setText((viewBookingForStr));

                if (BookingsManager.getNumNightsForView(viewBookingForStr).isBlank() || BookingsManager.getNumNightsForView(viewBookingForStr).isEmpty()) {
                    nightsStayingDisplayLabelBook.setText("No entry yet.");
                } else {
                    nightsStayingDisplayLabelBook.setText(BookingsManager.getNumNightsForView(viewBookingForStr));
                }
                if ((BookingsManager.getHotelDistanceForView(viewBookingForStr).isBlank() || (BookingsManager.getHotelDistanceForView(viewBookingForStr).isEmpty()))) {
                    hotelDistanceDisplayLabelBook.setText("No entry yet.");
                } else {
                    hotelDistanceDisplayLabelBook.setText(BookingsManager.getHotelDistanceForView(viewBookingForStr) + " km");
                }
                if ((BookingsManager.getNumPplForView(viewBookingForStr)).isBlank() || ((BookingsManager.getNumPplForView(viewBookingForStr)).isEmpty())) {
                    hotelNumGuestsDisplayLabelBook.setText("No entry yet.");
                } else {
                    hotelNumGuestsDisplayLabelBook.setText(BookingsManager.getNumPplForView(viewBookingForStr));
                }
                if ((BookingsManager.getTotalCostForView(viewBookingForStr)).isBlank() || ((BookingsManager.getTotalCostForView(viewBookingForStr)).isEmpty())) {
                    approxPriceDisplayLabelBook.setText("No entry yet.");
                } else {
                    approxPriceDisplayLabelBook.setText("R " + (BookingsManager.getTotalCostForView(viewBookingForStr)));
                }

                navigationScreenTabbedPane.add("Previous Booking Receipt", viewHotelBookingPanel);

                //REST BOOKING
            } else {
                String viewBookingForStr = (String) viewItemForCombo.getSelectedItem();
                restDisplayLabelBook.setText(viewBookingForStr);

                if ((BookingsManager.getRestGuestsForView(viewBookingForStr).isBlank()) || ((BookingsManager.getRestGuestsForView(viewBookingForStr).isEmpty()))) {
                    restNumGuestsDisplayLabelBook.setText("No entry yet.");
                } else {
                    restNumGuestsDisplayLabelBook.setText(BookingsManager.getRestGuestsForView(viewBookingForStr));
                }

                if ((BookingsManager.getTimeSelectedForView(viewBookingForStr)).isBlank() || ((BookingsManager.getTimeSelectedForView(viewBookingForStr)).isEmpty())) {
                    timeDisplayLabelBook.setText("No entry yet.");
                } else {
                    timeDisplayLabelBook.setText(BookingsManager.getTimeSelectedForView(viewBookingForStr));
                }

                if ((BookingsManager.getRestDistanceForView(viewBookingForStr).isBlank() || ((BookingsManager.getRestDistanceForView(viewBookingForStr).isEmpty())))) {
                    restDistDisplayLabelBook.setText("No entry yet.");
                } else {
                    restDistDisplayLabelBook.setText(BookingsManager.getRestDistanceForView(viewBookingForStr) + " km");
                }

                navigationScreenTabbedPane.add("Previous Booking Receipt", viewRestaurantBookingPanel);

            }

        }

    }//GEN-LAST:event_viewBookRevButtonActionPerformed

